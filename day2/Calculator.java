package week1.day2;

class Calculator {
	public int add(int num1, int num2, int num3) {
		int num = num1 + num2 + num3;
		return num;
	}

	public int sub(int nums1, int nums2) {
		int nums = nums1 - nums2;
		return nums;
	}

	public double mul(double numb1, double numb2) {
		double numb = numb1 * numb2;
		return numb;
	}

	public float div(float numf1, float numf2) {
		float numf = numf1 / numf2;
		return numf;
	}
}
